export const Authentication = {
  parse: (data: unknown): AuthenticationType => AuthenticationSchema.parse(preprocess(data)),

  safeParse: (data: unknown): { success: true; data: AuthenticationType } | { success: false; error: z.ZodError } => {
    try {
      const preprocessed = preprocess(data);
      return AuthenticationSchema.safeParse(preprocessed);
    } catch (error) {
      // Preprocessing error - convert to Zod error format
      return {
        success: false,
        error: new z.ZodError([{
          code: "custom",
          path: [],
          message: error instanceof Error ? error.message : "Preprocessing failed",
        }]),
      };
    }
  },

  schema: AuthenticationSchema,
};

export const FileActivity = {
  parse: (data: unknown): FileActivityType => FileActivitySchema.parse(preprocess(data)),

  safeParse: (data: unknown): { success: true; data: FileActivityType } | { success: false; error: z.ZodError } => {
    try {
      const preprocessed = preprocess(data);
      return FileActivitySchema.safeParse(preprocessed);
    } catch (error) {
      // Preprocessing error - convert to Zod error format
      return {
        success: false,
        error: new z.ZodError([{
          code: "custom",
          path: [],
          message: error instanceof Error ? error.message : "Preprocessing failed",
        }]),
      };
    }
  },

  schema: FileActivitySchema,
};
